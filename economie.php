			<h4>Précisions</h4>
			<p>Il est important de noter que YouTube puis Google pour sa filiale ne publient pas de données financières. Les sources visitées ne sont donc pas totalement fiables. Cependant, les nombreuses sources publiées et non-publiées donnent des estimations et spéculations très similaires.</p>

			<h2 id="partie1_sous_partie1"> Des débuts difficiles</h2>
			<p>YouTube, en février 2005, était  une petite startup de la Silicon Valley en Californie. À cette époque, le concept de pouvoir mettre en ligne publiquement des vidéos est presque inconnu et le grand public reste assez réservé à cette idée. YouTube ne possède que quelques serveurs sur la côte ouest des États-Unis. <br><br>
			Les recettes initiales de YouTube sont faibles au point que plusieurs fournisseurs d’accès internet notamment France Telecom (Orange actuellement) ne souhaitent pas négocier un prix de transit avec la startup car ils ne croient pas à sa réussite financière. <br><br>
			En 2005, YouTube aurait dégagé environ 20 millions de dollars mensuellement selon Wikipédia. Selon des spéculations de spécialistes, les coûts de fonctionnement totalisent eux 30 millions de dollars mensuels. La startup ne dégage pas de bénéfices faute de moyens pour investir, mais a compris à quel point la vidéo va donner un tournant à internet. Sa particularité est d’avoir su comment rendre ce partage accessible et ensuite en faire un besoin pour une population mondiale.</p> <br><br>

