			<h2 id="partie1_sous_partie2">Évolution du chiffre d'affaires</h2>
			<p>  Voici l’évolution du chiffre d’affaires de YouTube selon les données financières disponibles: <br><br>
				2012 : 2,4 milliards de dollars<br>    2013 : 3,5 milliards de dollars <br>
				2014 : 4 milliards de dollars  <br>    2016 : 4 milliards de dollars <br><br>
				Nous constatons que le chiffre d’affaires n’augmente plus entre 2014 et 2016. YouTube est implanté mondialement, mais aucune nouveauté importante sur son site n’a popularisé le site d’avantage. Par conséquent, le chiffre d’affaires stagne. <br>Bien que le chiffre d’affaires soit énorme, la filiale ne rapporte pas de bénéfices à Google. Effectivement, toutes les sources dont le Wall Street Journal et Le Monde s’accordent sur le point suivant : les coûts d’alimentation énergétique des serveurs de plus en plus nombreux équivalent quasiment à la totalité du chiffre d’affaires.
				<br><br>
				Il faut savoir que la plateforme génère entre 5 et 6 fois plus d’argent que celui qu’elle redistribue au créateur : 4,8€ contre 0,85€ pour 1000 vues d’une vidéo monétisée. Nous pouvons penser que le site se fait une grande marge de bénéfices, mais sur les 4,8€ qu’elle gagne, 55% (2,64€) sont destinés à la rémunération des créateurs. Et pour construire des ambassades tels que les YouTube Spaces, l’entreprise doit piocher dans les 45% restants. Il reste donc finalement que 2,16€ (au maximum) de revenus pour la plateforme. </p>

