			<h1 id="partie3" class="paddingPartie">YouTuber, un métier nouveau</h1>
			<h2 id="partie3_sous_partie1">Catégorie socioprofessionnelle</h2>
			<p>YouTuber est un métier encore émergent. C’est un métier au statut d’auto-entrepreneur si le créateur génère entre 13 788,86 € (taux actuel du SMIC) et 33100€ annuellement. Il devient entrepreneur s’il évolue et génère plus de 33 100€  et moins que 82800€ par an. Pour un auto-entrepreneur français sur YouTube,  22.70% du salaire est taxé par l’état, et plus de 50% (jusqu’à 65%, chiffres valable pour 2017, 2018 et 2019) pour les entrepreneurs. </p>
			<br>
			<h2 id="partie3_sous_partie2">Difficultés du métier</h2>
			<p> Ces statuts socioprofessionnels sont néanmoins particuliers, car nous avons pu constater que la rémunération est entièrement décidée par le site et peut changer à son gré. La difficulté principale que ces (auto)entrepreneurs rencontrent est celle du manque de droits et de visibilité vis-à-vis du site, c’est-à-dire qu’ils ne peuvent pas prévoir leur travail sur le long terme à cause d’éventuelles nouvelles mesures prises par celui-ci.</p>

